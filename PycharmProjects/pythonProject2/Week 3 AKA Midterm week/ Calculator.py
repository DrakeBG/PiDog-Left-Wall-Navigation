def calculator(float1, float2, string):
    if (string == "+"):
        return float1 + float2
    elif (string == "-"):
        return float1 + float2
    elif (string == "/"):
        return float1 + float2
    elif (string == "*"):
        return float1 + float2
    elif (string == "%"):
        return float1 + float2
    elif (string == "//"):
        return float1 + float2
num1 = 7.3
num2 = 6.8
oper = "+"
print(calculator(num1, num2, oper))

